#include <unistd.h>

int main(int argc, char **argv)
{
	if (argc == 2)
	{
	int c = 0;
		while (argv[1][c])
		{
			if (argv[1][c] == 'a')
				write(1, "a", 1);
			++c;
		}
		write(1, "\n", 1);
	}
	else
		write(1, "a\n", 2);
	return (0);
}
